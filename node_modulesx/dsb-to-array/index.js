module.exports = (function(){
/***********************************************************************/  
  
  /* 
   * @method toArray
   * creates an array of an objects values (useful for [arguments])
   * @return array
   */
  
/*---------------------------------------------------------------------*/

  var toArray = function(o){ 
    var a = [], o = o || this; 
    if( o instanceof Array ) return o;
    for(var i in o) a.push(o[i]); 
    return a; 
  };
  
/***********************************************************************/

  return toArray;
  
/*---------------------------------------------------------------------*/  
}());